#problematica: 
#El usuario quiere saber cuantas letras tiene una palabra

#Algoritmo
#1) Solicitar al usuario que ingrese una palabra
palabra_ingresada = input("por favor ingrese la palabra:")

#2) Contar letras en la palabra ingresada
cantidad_letras = len(palabra_ingresada)

#3) Mostrar cantidad de letras que tiene la palabra ingresada
print ("la palabra ingresada tiene", cantidad_letras , "letras")

